class Math():

    def even(num):
        if num%2==0:
            return ('%d is an even number'%(num))
        else:
            return ('%d is not an even number'%(num))
        
    def odd(num):

        if num%2!=0:
            return ('%d is an odd number'%(num))
        else:
            return ('%d is not an odd number'%(num))

    def power(num1,num2):
        return ('%d * %d is %d'%(num1,num2,num1**num2))

    
        
            
            
        


    
